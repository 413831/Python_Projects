miVariable = 20
print(miVariable)

# Incremento con reasignacion
miVariable += 1
print(miVariable)

# Decremento con reasignacion
miVariable -= 1
print(miVariable)

# Multiplicacion con reasignacion
miVariable *= 4
print(miVariable)

# Division con reasignacion
miVariable /= 3
print(miVariable)

# Division entera con reasignacion
miVariable //= 3
print(miVariable)

# Modulo con reasignacion
miVariable %= 3
print(miVariable)

# Potencia con reasignacion
miVariable **= 2
print(miVariable)

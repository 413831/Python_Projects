operandoA = 3
operandoB = 2

# Suma

suma = operandoA + operandoB
print(suma)
print(f'Resultado suma: {suma}')

# Resta
resta = operandoA - operandoB
print(f'Resultado resta: {resta}')

# Multiplicacion
multiplicacion = operandoA * operandoB
print(f'Resultado de la multiplicacion: {multiplicacion}')

# Division
division = operandoA / operandoB
print(f'Resultado de la division: {division}')

# Division entero
divisionEntero = operandoA // operandoB
print(f'Resultado entero de la division: {divisionEntero}')

# Modulo
modulo = operandoA % operandoB
print(f'Resultado del resto de la division: {modulo}')

# Potencia
potencia = operandoA ** operandoB
print(f'Resultado de potencia: {potencia}')


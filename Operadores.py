x = 2
y = 5

# OPERADORES ARITMETICOS

# Suma " + "
resultado = x + y
print("Este es el resultado de x + y =", resultado)

# Resta " - "
resultado = x - y
print("Este es el resultado x - y =", resultado)

# Multiplicación " * "
resultado = x * y
print("Este es el resultado x * y =", resultado)

# División " / "
resultado = x / y
print("Este es el resultado x / y =", resultado)

# Producto entero de una división " // "
resultado = x // y
print("Este es el resultado x // y =", resultado)

# Potencia " ** "
resultado = x ** y
print("Este es el resultado x * y =", resultado)

# OPERADORES LOGICOS

# Mayor que " > "
print("x es mayor que y = ", x > y)

# Menor que " < "
print("x es menor que y = ", x < y)

# Igual a  " == "
print("x es igual a y = ", x == y)

# No igual a " != "
print("x no es igual a y = ", x != y)

# Mayor o igual que " >= "
print("x es mayor o igual a y = ", x >= y)

# Menor o igual que " <= "
print("x es menor o igual a y = ", x <= y)
# Caracteres de escape
resultado = 'Hola Mundo'
print(f'Resultado: {resultado}')

resultado = 'Se va a eliminar el punto.\b'
print(f'Resultado: {resultado}')

# Caracter \
resultado = 'c:\\directorio\\juan'
print(f'Resultado: {resultado}')

# raw string
resultado = r'Cadena con \n salto de linea'
print(f'Resultado: {resultado}')

resultado = r'c:\banco\juan'
print(f'Resultado: {resultado}')
